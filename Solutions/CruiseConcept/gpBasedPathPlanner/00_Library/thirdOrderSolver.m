function [x] = thirdOrderSolver(a,b,c,d)
A = (27*a^2*c - 9*a*b^2)/(27*a^3);
B = (2*b^3-9*a*b*c+27*a^2*d) / (27*a^3);
D = (B/2)^2+(A/3)^3;

if (D<0)
    x(1) = -b/(3*a)+2*(-A/3)^0.5*cos(1/3*acos((-B/2)/((-A/3)^3)^0.5));
    x(2) = -b/(3*a)+2*(-A/3)^0.5*cos(2*pi()/3+1/3*acos((-B/2)/((-A/3)^3)^0.5));
    x(3) = -b/(3*a)+2*(-A/3)^0.5*cos(2*pi()/3-1/3*acos((-B/2)/((-A/3)^3)^0.5));
else
    x(1) = -b/(3*a) + (-B/2+D^0.5)^(1/3) + (-B/2-D^0.5)^(1/3);
    %x(2) = -b/(3*a) - 1/2*((-B/2+D^0.5)^1/3+(-B/2-D^1/2)^1/3) + i*3^0.5/2
end

end

